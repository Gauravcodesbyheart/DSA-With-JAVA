public class evenorodd {
    public static void main (String [] args) 
    {
        int x = 5;
        if (x % 2 == 0)
        {
            System.out.println("The number "+ x +" is the even number");
        }
        else 
        {
            System.out.println("The number "+ x +" is the odd number");
        }
    }
    
}
